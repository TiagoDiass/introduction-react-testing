export default class Calculator {
  static sum(n1: number, n2: number) {
    return n1 + n2;
  }

  static subtract(n1: number, n2: number) {
    return n1 - n2;
  }

  static multiply(n1: number, n2: number) {
    return n1 * n2;
  }

  static divide(n1: number, n2: number) {
    return n1 / n2;
  }
}
